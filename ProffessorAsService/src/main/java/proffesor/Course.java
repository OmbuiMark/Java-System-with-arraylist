package proffesor;

class Course {
        String name;
        String description;
        String professor;
        int duration;
        String schedule;
        String location;

        public Course(String name, String description, String professor, int duration, String schedule, String location) {
            this.name = name;
            this.description = description;
            this.professor = professor;
            this.duration = duration;
            this.schedule = schedule;
            this.location = location;
        }
    }





